/// Number of random bytes drawn for a certificate serial — 128 bits, well
/// above the CA/B Forum 64-bit floor (research Finding 10).
const SERIAL_BYTES: usize = 16;

/// In-memory [`Ca`] adapter for DST.
///
/// Holds the fixture root material as opaque bytes and a seeded [`Entropy`]
/// source. `Send + Sync` (the `Arc<dyn Entropy>` is `Send + Sync`), matching
/// the sibling sim adapters, so it can be shared across async tasks.
pub struct SimCa {
    entropy: Arc<dyn Entropy>,
}

impl SimCa {
    /// Construct a `SimCa` over a required [`Entropy`] source.
    ///
    /// No builder, no default — the entropy dependency is mandatory at
    /// construction so a test that forgets to inject it fails to compile
    /// (`.claude/rules/development.md` § "Port-trait dependencies"). The
    /// fixture root key/cert are embedded `const`s, not constructor inputs.
    #[must_use]
    pub fn new(entropy: Arc<dyn Entropy>) -> Self {
        Self { entropy }
    }

    /// Draw a fresh certificate serial from the seeded entropy source.
    ///
    /// Lowercase hex of [`SERIAL_BYTES`] random bytes — satisfies
    /// [`CertSerial`]'s validator (even-length lowercase hex, ≤20 bytes). The
    /// draw is the load-bearing seed dependency: two `SimCa` over the same
    /// seed draw identical bytes, so the whole handle is byte-identical.
    fn draw_serial(&self) -> CertSerial {
        let mut bytes = [0u8; SERIAL_BYTES];
        self.entropy.fill(&mut bytes);
        let hex = bytes.iter().fold(String::with_capacity(SERIAL_BYTES * 2), |mut acc, b| {
            use std::fmt::Write as _;
            let _ = write!(acc, "{b:02x}");
            acc
        });
        CertSerial::new(&hex).unwrap_or_else(|_| {
            unreachable!("{SERIAL_BYTES}-byte lowercase hex is a valid CertSerial")
        })
    }
}

impl Ca for SimCa {
    fn root(&self) -> Result<RootCaHandle, CaError> {
        Ok(RootCaHandle::new(
            CaCertPem::new(FIXTURE_ROOT_CERT_PEM.to_owned()),
            CaCertDer::new(FIXTURE_ROOT_CERT_DER.to_vec()),
            self.draw_serial(),
            CaKeyPem::new(FIXTURE_ROOT_KEY_PEM.to_owned()),
        ))
    }

    fn issue_intermediate(
        &self,
        _node: &overdrive_core::NodeId,
    ) -> Result<IntermediateHandle, CaError> {
        // Opaque fixture intermediate: a real `openssl`-minted P-256 cert
        // signed by `FIXTURE_ROOT_KEY_PEM` (pathLen=0, keyCertSign critical),
        // so its issuer == the fixture root's subject and it chains to the
        // root anchor. The serial is drawn through the seeded `Entropy` port —
        // the load-bearing DST determinism dependency. Two `SimCa` over the
        // same seed draw the identical serial here, so the whole handle is
        // byte-identical. Single-node (Phase 2.6): the `node` does not vary the
        // fixture material.
        Ok(IntermediateHandle::new(
            CaCertPem::new(FIXTURE_INTERMEDIATE_CERT_PEM.to_owned()),
            CaCertDer::new(FIXTURE_INTERMEDIATE_CERT_DER.to_vec()),
            self.draw_serial(),
            CaKeyPem::new(FIXTURE_INTERMEDIATE_KEY_PEM.to_owned()),
        ))
    }

    fn issue_svid(&self, req: &SvidRequest) -> Result<SvidMaterial, CaError> {
        // Delegate the single-URI-SAN cardinality + CA:FALSE leaf-profile
        // DECISION to the shared core `CertSpec::svid` policy (ADR-0063 D5) —
        // the sim does NOT fork the SAN rule. The SAN projection is derived
        // from the request's `SpiffeId`; a projection that is not exactly one
        // URI SAN is rejected by the policy with `CertSpecError::InvalidSan`,
        // which passes through to `CaError::Policy` via `#[from]` BEFORE any
        // fixture material is returned. The produced spec's subject is the sole
        // URI SAN the issued leaf carries.
        let spec = CertSpec::svid(vec![req.spiffe_id().clone()])?;

        // Opaque fixture leaf: a real `openssl`-minted P-256 cert signed by the
        // fixture intermediate (CA:FALSE, keyUsage=digitalSignature critical,
        // exactly one `spiffe://` URI SAN), so it chains root → intermediate →
        // leaf. The serial is drawn through the seeded `Entropy` port — the
        // load-bearing DST determinism dependency, exactly as the host adapter
        // will draw it via `OsEntropy`. Two `SimCa` over the same seed draw the
        // identical serial here, so the whole handle is byte-identical.
        //
        // FIXED-IDENTITY LIMITATION: the returned `spiffe_id` below tracks the
        // request (`spec.subject()` == `req.spiffe_id()`), but the cert bytes
        // carry the FROZEN fixture SAN `spiffe://overdrive.local/workload/sim-svid`
        // — the sim holds them opaquely and cannot re-mint with an arbitrary SAN
        // (research Finding 11). So for any non-fixture request the cert-byte SAN
        // diverges from `spiffe_id()`, and the `Ca::issue_svid` postcondition
        // ("exactly one URI SAN equal to `req.spiffe_id()`") holds at the byte
        // level ONLY for the fixture identity. A DST test that parses
        // `cert_der()` to check the SAN must issue for the fixture identity (as
        // `ca_equivalence` does); one that only observes `spiffe_id()` / serial
        // determinism is unaffected. See `FIXTURE_SVID_CERT_PEM`.
        Ok(SvidMaterial::new(
            CaCertPem::new(FIXTURE_SVID_CERT_PEM.to_owned()),
            CaCertDer::new(FIXTURE_SVID_CERT_DER.to_vec()),
            self.draw_serial(),
            spec.subject().clone(),
        ))
    }

    fn trust_bundle(&self) -> Result<TrustBundle, CaError> {
        // The trust anchor is the fixture root certificate (opaque bytes); the
        // chain material is the fixture node-intermediate, included as UNTRUSTED
        // chain material so a relying party can build root → intermediate → leaf
        // and anchor trust on the root (ADR-0063 D1 wire-format). The fixture
        // leaf (`FIXTURE_SVID_CERT_*`) chains to this intermediate, which chains
        // to this root, so a sim-issued leaf "verifies" against this bundle on
        // the sim's opaque-byte path (the sim cannot sign — research Finding 11).
        // Composition order is root-anchor-first; the same `TrustBundle::new`
        // shape the host adapter composes, so the COMPOSITION SHAPE is equivalent
        // across adapters even though the concrete cert bytes differ.
        Ok(TrustBundle::new(
            CaCertPem::new(FIXTURE_ROOT_CERT_PEM.to_owned()),
            Some(CaCertPem::new(FIXTURE_INTERMEDIATE_CERT_PEM.to_owned())),
        ))
    }
}
